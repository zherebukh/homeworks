number_1 = 10
number_2 = 5

# Оператори порівняння нам повертають тру або фолс
print(number_1 == number_2) # Дорівнює
print(number_1 != number_2) # Hе дорівнює
print(number_1 > number_2)
print(number_1 >= number_2)
print(number_1 < number_2)
print(number_1 <= number_2)

# Може бути ще так. Пріоритет операторів як в математиці. Можна також брати в душки
print(20 / 4 <= 2 * 2 + 10)

# Також можна порівнювати стрінги (чи те саме значення)
print('helle' == 'Hello')

# Списки
print(['1', 2] == ['1', 2])




